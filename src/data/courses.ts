export function getCourseById(id: string): Course | undefined {
  return courses.find(c => c.id === id);
}

export function getCoursesByCategory(category: string): Course[] {
  return courses.filter(c => c.category === category);
}

export function getPrerequisiteCourses(course: Course): Course[] {
  return course.prerequisites
    .map(id => getCourseById(id))
    .filter((c): c is Course => c !== undefined);
}

export function getCoursesForSkills(skills: string[]): Course[] {
  const skillLower = skills.map(s => s.toLowerCase());
  return courses.filter(course =>
    course.skillsTaught.some(skill =>
      skillLower.some(s => skill.toLowerCase().includes(s) || s.includes(skill.toLowerCase()))
    )
  );
}
